package j01_출력;

public class PrintTest2 {

	public static void main(String[] args) {
		int a;
		a = 10;
		System.out.println(a);
		// int a; 정수(변수 선언)
		// 변수 - 가리킨다
		// 클린코드 : a = 10 (*스페이스바)
		
		a = 20;
		System.out.println(a);
		
		int b = 100;
		System.out.println(b);
		
		double c = 3.14;
		System.out.println(c);
		
		char lastName = '최';
		// char firstName = '준혁';
		System.out.println(lastName);
		// char '문자'
		
		String name = "최준혁";
		System.out.println(name);
		// String "문자열"
		// String 클래스 자료형 첫글자 대문자(기본자료형X)
		
		boolean tt = true;
		boolean ff = false;
		System.out.println(tt);
		System.out.println(ff);
		// boolean ""없이 기입, t소문자 기입
		
		
	}

}
